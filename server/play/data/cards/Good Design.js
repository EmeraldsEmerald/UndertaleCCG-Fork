const ListenerReceiver = require('../../classes/ListenerReceiver.js').ListenerReceiver
card={
    origAttack: 2,
    origHP: 3,
    baseKeywords: [],
    geoCost: 4,
    type: "character",
    baseText: [{ type: "plainText", value: "When this card is generated by an effect, reveal it and give all ally monsters +1/+1: " }],
    imageLink: "",
    rarity: 3,
    onCardCreation: function () {
        if (!this.player.game.started || this.player.noCharacters) {
            return
        }
        this.player.addDualAnimation("triggerEffect", { card: this.getSendableCopy() }, 700)
        for (let i = 0; i < this.player.slots.length; i++) {
            if (this.player.slots[i] != null) {
                this.player.slots[i].applyBuff({ hp: 1, attack: 1 })
            }
        }
    },
    factions: [2, 2],
}
module.exports={card}